/**
 * commit.service.ts isolated tests.
 *
 * Uses setSpawnFn() to inject a mock spawn implementation so no real git
 * process is ever started. Uses setCommitQueryFn() to inject a mock query
 * function for commit message generation.
 */

import assert from 'node:assert/strict';
import test from 'node:test';
import { EventEmitter } from 'node:events';

import {
  commitWithAutopilot,
  setCommitQueryFn,
  setSpawnFn,
} from '../services/commit.service.js';
import type { CommitOptions } from '../services/commit.service.js';
import type { WebSocketWriter } from '@/modules/websocket/index.js';

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/** Minimal WebSocketWriter mock. */
function makeWs(): WebSocketWriter & { sent: unknown[] } {
  const sent: unknown[] = [];
  return {
    send(data: unknown) {
      sent.push(data);
    },
    sent,
  } as unknown as WebSocketWriter & { sent: unknown[] };
}

/** Default CommitOptions for tests. */
function makeOpts(overrides: Partial<CommitOptions> = {}): CommitOptions {
  return {
    cwd: '/fake/cwd',
    ws: makeWs(),
    sessionId: 'test-commit-session',
    ...overrides,
  };
}

/**
 * Build a mock spawn that returns preset stdout/stderr/exitCode per command.
 *
 * `responses` maps `args[0]` (the git subcommand) to { stdout, stderr, code }.
 * Unmatched commands default to { stdout: '', stderr: '', code: 0 }.
 *
 * Also records every call as { command, args } in `calls`.
 */
function makeMockSpawn(
  responses: Record<string, { stdout?: string; stderr?: string; code?: number }> = {},
) {
  const calls: Array<{ command: string; args: string[] }> = [];

  const mockSpawn = (command: string, args: string[], _opts: unknown) => {
    calls.push({ command, args });

    const subcommand = args[0] ?? '';
    const preset = responses[subcommand] ?? {};
    const stdout = preset.stdout ?? '';
    const stderr = preset.stderr ?? '';
    const code = preset.code ?? 0;

    // Build a fake ChildProcess using EventEmitter
    const child = new EventEmitter() as EventEmitter & {
      stdout: EventEmitter;
      stderr: EventEmitter;
    };
    child.stdout = new EventEmitter();
    child.stderr = new EventEmitter();

    // Emit data and close asynchronously so the promise chain works
    setImmediate(() => {
      if (stdout) child.stdout.emit('data', Buffer.from(stdout));
      if (stderr) child.stderr.emit('data', Buffer.from(stderr));
      child.emit('close', code);
    });

    return child;
  };

  return { mockSpawn: mockSpawn as unknown as Parameters<typeof setSpawnFn>[0], calls };
}

// ---------------------------------------------------------------------------
// T1: commitWithAutopilot never calls git push
// ---------------------------------------------------------------------------

test('commitWithAutopilot never invokes git push', async () => {
  const { mockSpawn, calls } = makeMockSpawn({
    status: { stdout: 'M  src/foo.ts\n', code: 0 },
    add: { stdout: '', code: 0 },
    commit: { stdout: '', code: 0 },
    'rev-parse': { stdout: 'abc1234\n', code: 0 },
  });

  setSpawnFn(mockSpawn);
  setCommitQueryFn(null as unknown as Parameters<typeof setCommitQueryFn>[0]);

  await commitWithAutopilot(makeOpts());

  const pushCalls = calls.filter(
    (c) => c.command === 'git' && c.args.includes('push'),
  );
  assert.equal(pushCalls.length, 0, 'git push must never be called');
});

// ---------------------------------------------------------------------------
// T2: generated commit message contains required trailers
// ---------------------------------------------------------------------------

test('commit message generated by queryFn contains Directive and Confidence trailers', async () => {
  const expectedMessage =
    'feat(auth): add token refresh\n\nDirective: autopilot-commit\nConfidence: high';

  const { mockSpawn, calls } = makeMockSpawn({
    status: { stdout: 'M  src/auth.ts\n', code: 0 },
    add: { stdout: '', code: 0 },
    commit: { stdout: '', code: 0 },
    'rev-parse': { stdout: 'deadbeef\n', code: 0 },
  });

  setSpawnFn(mockSpawn);

  // Inject a queryFn that sends the expected message as assistant text
  setCommitQueryFn(async (_prompt, _opts, ws) => {
    ws.send(
      JSON.stringify({
        type: 'assistant',
        content: [{ type: 'text', text: expectedMessage }],
      }),
    );
  });

  await commitWithAutopilot(makeOpts());

  // Find the git commit -m call and check the message argument
  const commitCall = calls.find(
    (c) => c.command === 'git' && c.args[0] === 'commit',
  );
  assert.ok(commitCall !== undefined, 'git commit should have been called');

  const msgIndex = commitCall.args.indexOf('-m');
  assert.ok(msgIndex !== -1, 'git commit should have -m flag');
  const actualMessage = commitCall.args[msgIndex + 1];

  assert.ok(
    actualMessage.includes('Directive: autopilot-commit'),
    `commit message should contain 'Directive: autopilot-commit', got: ${actualMessage}`,
  );
  assert.ok(
    actualMessage.includes('Confidence: high'),
    `commit message should contain 'Confidence: high', got: ${actualMessage}`,
  );
});

// ---------------------------------------------------------------------------
// T3: unresolvedFindings appear in the prompt and Not-tested in commit message
// ---------------------------------------------------------------------------

test('unresolvedFindings are included in the prompt and Not-tested trailer in commit message', async () => {
  let capturedPrompt = '';
  const messageWithNotTested =
    'fix(security): address review findings\n\nDirective: autopilot-commit\nNot-tested: XSS in input handler';

  const { mockSpawn, calls } = makeMockSpawn({
    status: { stdout: 'M  src/input.ts\n', code: 0 },
    add: { stdout: '', code: 0 },
    commit: { stdout: '', code: 0 },
    'rev-parse': { stdout: 'cafebabe\n', code: 0 },
  });

  setSpawnFn(mockSpawn);

  setCommitQueryFn(async (prompt, _opts, ws) => {
    capturedPrompt = prompt;
    ws.send(
      JSON.stringify({
        type: 'assistant',
        content: [{ type: 'text', text: messageWithNotTested }],
      }),
    );
  });

  await commitWithAutopilot(
    makeOpts({
      unresolvedFindings: [
        { id: 'xss1', severity: 'high', message: 'XSS in input handler', file: 'src/input.ts' },
      ],
    }),
  );

  // Prompt should mention the finding
  assert.ok(
    capturedPrompt.includes('XSS in input handler'),
    `prompt should include finding message, got: ${capturedPrompt.slice(0, 200)}`,
  );
  assert.ok(
    capturedPrompt.includes('Not-tested') || capturedPrompt.includes('未解决'),
    'prompt should mention unresolved findings section',
  );

  // Commit message should contain Not-tested trailer
  const commitCall = calls.find(
    (c) => c.command === 'git' && c.args[0] === 'commit',
  );
  assert.ok(commitCall !== undefined, 'git commit should have been called');
  const msgIndex = commitCall.args.indexOf('-m');
  const actualMessage = commitCall.args[msgIndex + 1];
  assert.ok(
    actualMessage.includes('Not-tested'),
    `commit message should contain Not-tested trailer, got: ${actualMessage}`,
  );
});

// ---------------------------------------------------------------------------
// T4: empty git status → skipped=true, git commit never called
// ---------------------------------------------------------------------------

test('empty git status returns skipped=true and never calls git commit', async () => {
  const { mockSpawn, calls } = makeMockSpawn({
    status: { stdout: '', code: 0 }, // empty — nothing to commit
  });

  setSpawnFn(mockSpawn);
  setCommitQueryFn(null as unknown as Parameters<typeof setCommitQueryFn>[0]);

  const result = await commitWithAutopilot(makeOpts());

  assert.equal(result.skipped, true, 'should return skipped=true when nothing to commit');

  const commitCalls = calls.filter(
    (c) => c.command === 'git' && c.args[0] === 'commit',
  );
  assert.equal(commitCalls.length, 0, 'git commit should not be called when nothing to commit');
});

// ---------------------------------------------------------------------------
// T5: git commit exits with code 1 → throws an error
// ---------------------------------------------------------------------------

test('git commit exit code 1 causes commitWithAutopilot to throw', async () => {
  const { mockSpawn } = makeMockSpawn({
    status: { stdout: 'M  src/broken.ts\n', code: 0 },
    add: { stdout: '', code: 0 },
    commit: { stdout: '', stderr: 'error: nothing to commit', code: 1 },
  });

  setSpawnFn(mockSpawn);
  setCommitQueryFn(null as unknown as Parameters<typeof setCommitQueryFn>[0]);

  let threw = false;
  try {
    await commitWithAutopilot(makeOpts());
  } catch (err) {
    threw = true;
    assert.ok(err instanceof Error, 'thrown value should be an Error');
    assert.ok(
      (err as Error).message.includes('commit') || (err as Error).message.includes('exit'),
      `error message should mention commit failure, got: ${(err as Error).message}`,
    );
  }

  assert.ok(threw, 'commitWithAutopilot should throw when git commit fails');
});
